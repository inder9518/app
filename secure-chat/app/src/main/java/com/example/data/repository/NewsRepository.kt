    // Refresh articles locally using the fake dataset of 15 articles per category, loading 10 at a time cyclic
    suspend fun refreshArticles(category: String) {
        delay(800) // Simulate a slight realistic loading animation for professional UX

        val templates = getTemplatesForCategory(category)
        val images = getImagesForCategory(category)
        
        // Get or initialize the offset page count for this category
        val currentOffset = categoryOffsets[category] ?: 0
        val startingIndex = (currentOffset * 10) % templates.size

        val pagedArticles = mutableListOf<NewsEntity>()

        for (i in 0 until 10) {
            val templateIndex = (startingIndex + i) % templates.size
            val template = templates[templateIndex]
            val imageIndex = templateIndex % images.size
            val sourceIndex = templateIndex % sources.size

            // Generate an ISO formatted publishedAt value with a descending delay based on list layout sorting order
            val sdf = SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss'Z'", Locale.getDefault()).apply {
                timeZone = TimeZone.getTimeZone("UTC")
            }
            // Add minute differences so newest items appear at the top
            val timeOffset = (9 - i) * 10 * 60 * 1000L // Newer published time to oldest published time
            val date = Date(System.currentTimeMillis() - timeOffset)
            val publishedAt = sdf.format(date)

            pagedArticles.add(
                NewsEntity(
                    url = "https://newsbharat.com/article/${category}/${templateIndex}",
                    title = template.first,
                    description = template.second,
                    sourceName = sources[sourceIndex],
                    urlToImage = images[imageIndex],
                    publishedAt = publishedAt,
                    category = category,
                    cachedAt = System.currentTimeMillis()
                )
            )
        }

        // Increment pagination page count cyclic for next refresh click
        categoryOffsets[category] = currentOffset + 1

        if (pagedArticles.isNotEmpty()) {
            newsDao.clearByCategory(category)
            newsDao.insertArticles(pagedArticles)
        }
    }

    // Search query locally using all the fake articles in our local pool
    suspend fun searchOnlineArticles(query: String): List<NewsEntity> {
        delay(600) // Local search simulated processing delay for clean transitions
        val allArticles = mutableListOf<NewsEntity>()
        val categories = listOf("general", "business", "technology", "sports", "entertainment", "science", "health")
        
        categories.forEach { category ->
            val templates = getTemplatesForCategory(category)
            val images = getImagesForCategory(category)
            templates.forEachIndexed { index, template ->
                val sdf = SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss'Z'", Locale.getDefault()).apply {
                    timeZone = TimeZone.getTimeZone("UTC")
                }
                val timeOffset = index * 30 * 60 * 1000L
                val date = Date(System.currentTimeMillis() - timeOffset)
                val publishedAt = sdf.format(date)

                allArticles.add(
                    NewsEntity(
                        url = "https://newsbharat.com/article/${category}/${index}",
                        title = template.first,
                        description = template.second,
                        sourceName = sources[index % sources.size],
                        urlToImage = images[index % images.size],
                        publishedAt = publishedAt,
                        category = category,
                        cachedAt = System.currentTimeMillis()
                    )
                )
            }
        }

        return allArticles.filter {
            it.title.contains(query, ignoreCase = true) ||
            it.description.contains(query, ignoreCase = true)
        }
    }
